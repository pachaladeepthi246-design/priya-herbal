// Helper functions
export function getProductBySlug(slug: string): HerbalProduct | undefined {
    return herbalProducts.find(p => p.slug === slug)
}

export function getProductsByCategory(category: string): HerbalProduct[] {
    return herbalProducts.filter(p => p.category === category)
}

export function getFeaturedProducts(): HerbalProduct[] {
    return herbalProducts.filter(p => p.featured)
}

export function getBestsellerProducts(): HerbalProduct[] {
    return herbalProducts.filter(p => p.bestseller)
}

